import 'dart:ui';

class LeadModel {
  final String avatar;
  final String name;
  final String label;
  final String icon;
  final Color color;
  final String date;
  final int amount;

  LeadModel({
    required this.avatar,
    required this.name,
    required this.label,
    required this.icon,
    required this.color,
    required this.date,
    required this.amount,
  });
}
